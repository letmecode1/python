
########## Numbers ##########

print(2+2) # addition
print(10-2) # subtraction
print(9*9) # multiplication
print(5/3) # division
print(5//3) # floor division, always returns a number without a remainder
print(8%3) # Modulo division, returns the remainder
print(2 ** 3) # exponent


########## Converting Strings to Numbers ##########
print(type(str(1.1)))
print(type(int("10")))
print(type(int(5.9999)))
print(type(float("5.6")))
print(type(float(5)))
